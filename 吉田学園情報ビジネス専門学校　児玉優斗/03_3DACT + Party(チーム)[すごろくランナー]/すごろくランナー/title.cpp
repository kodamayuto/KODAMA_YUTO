// 更新処理
//=============================================================================
void UpdateTitle(void)
{
	// カメラの更新処理
	UpdateCamera();

	//ライトの更新処理
	UpdateLight();

	// マスの更新処理
	UpdateMasu();

	// コースの更新処理
	UpdateCourse();

	// 障害物の更新処理
	UpdateObstacle();

	//背景の木の更新処理
	UpdateWoodBG();

	//メッシュフィールドの更新処理
	UpdateMeshField();

	//空の更新処理
	UpdateSky();

	//草の更新処理
	UpdateLeaf();

	//タイトルロゴの更新処理
	UpdateTitleLogo();

	FADE fade;
	fade = GetFade();

	g_nCounterTitle++;

	if (g_nCounterTitle >= TRANSITION_MODE)
	{// ある程度時間がたった
		if (GetKeyboardTrigger(DIK_RETURN) == true && fade == FADE_NONE)
		{// 決定ボタンが押された
			SetFade(MODE_SELECT);
		}

		for (int nCntJoyPad = 0; nCntJoyPad < 8; nCntJoyPad++)
		{
			if (GetJoyPadTrigger(nCntJoyPad, 0) == TRUE && fade == FADE_NONE)
			{
				SetFade(MODE_SELECT);
			}
		}
	}

	if (g_nCounterTitle % 800 == 0 && fade == FADE_NONE)
	{// 一定時間がたつ
		SetFade(MODE_RANKING);
	}
}
